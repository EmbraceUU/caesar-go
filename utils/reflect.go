package utils

import (
	"encoding/json"
	"fmt"
	"github.com/shopspring/decimal"
	"math"
	"strconv"
	"time"
)

func AnyToString(value interface{}) string {
	var key string
	if value == nil {
		return key
	}

	switch value.(type) {
	case float64:
		ft := value.(float64)
		if math.IsNaN(ft) {
			return ""
		}
		if math.IsInf(ft, 0) {
			return ""
		}
		key = decimal.NewFromFloat(ft).String()
	case float32:
		ft := value.(float32)
		key = decimal.NewFromFloat32(ft).String()
	case int:
		it := value.(int)
		key = strconv.Itoa(it)
	case uint:
		it := value.(uint)
		key = strconv.Itoa(int(it))
	case int8:
		it := value.(int8)
		key = strconv.Itoa(int(it))
	case uint8:
		it := value.(uint8)
		key = strconv.Itoa(int(it))
	case int16:
		it := value.(int16)
		key = strconv.Itoa(int(it))
	case uint16:
		it := value.(uint16)
		key = strconv.Itoa(int(it))
	case int32:
		it := value.(int32)
		key = strconv.Itoa(int(it))
	case uint32:
		it := value.(uint32)
		key = strconv.Itoa(int(it))
	case int64:
		it := value.(int64)
		key = strconv.FormatInt(it, 10)
	case uint64:
		it := value.(uint64)
		key = strconv.FormatUint(it, 10)
	case string:
		key = value.(string)
	case time.Time:
		key = fmt.Sprintf("%v", value.(time.Time).Format("2006-01-02 15:04:05"))
	case []byte:
		key = string(value.([]byte))
	default:
		newValue, _ := json.Marshal(value)
		key = string(newValue)
	}

	return key
}
